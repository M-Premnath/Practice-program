import pywhatkit

# Schedule the message
pywhatkit.sendwhatmsg('+919629565554', 'Good night', 20, 8, wait_time=60)


# This will send the "Good night" message to the mobile number prefixed with +91 at hrs= 20 and min = 8
# The wait_time is set to 60 seconds, which means the message will be sent 60 seconds
